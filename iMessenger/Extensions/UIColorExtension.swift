import Foundation
import UIKit

extension UIColor {
    static func appleBlue() -> UIColor {
        return UIColor(red: 14.0/255, green: 122.0/255, blue: 254.0/255, alpha: 1.0)
    }
    
    static func bubbleGray() -> UIColor {
        return UIColor(red: 225/255, green: 225/255, blue: 225/255, alpha: 1.0)
    }
}
